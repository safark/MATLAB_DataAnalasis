function battery_app_gui

dataTable = table();
textColor = [0.00, 0.00, 0.00];
inputBackgroundColor = [1.00, 1.00, 1.00];
buttonColor = [0.00, 0.45, 0.74];
buttonTextColor = [1.00, 1.00, 1.00];
summaryPanelColor = [1.00, 1.00, 1.00];

fig = uifigure( ...
    "Name", "Battery Data Visualizer", ...
    "Position", [100, 100, 1120, 680], ...
    "Color", [0.97, 0.97, 0.98]);

uibutton(fig, "push", ...
    "Text", "Load CSV", ...
    "Position", [20, 635, 110, 32], ...
    "BackgroundColor", buttonColor, ...
    "FontColor", buttonTextColor, ...
    "ButtonPushedFcn", @onLoadCsv);

uibutton(fig, "push", ...
    "Text", "Generate Synthetic Data", ...
    "Position", [145, 635, 180, 32], ...
    "BackgroundColor", buttonColor, ...
    "FontColor", buttonTextColor, ...
    "ButtonPushedFcn", @onGenerateSynthetic);

uilabel(fig, ...
    "Text", "Plot:", ...
    "Position", [350, 640, 35, 22], ...
    "FontColor", textColor);

plotDropdown = uidropdown(fig, ...
    "Items", { ...
        'SOC vs Time', ...
        'Voltage vs Time', ...
        'Current vs Time', ...
        'Temperature vs Time'}, ...
    "Value", 'SOC vs Time', ...
    "Position", [390, 635, 180, 32], ...
    "BackgroundColor", inputBackgroundColor, ...
    "FontColor", textColor, ...
    "ValueChangedFcn", @onControlsChanged);

uilabel(fig, ...
    "Text", "Start Time (s):", ...
    "Position", [590, 640, 92, 22], ...
    "FontColor", textColor);

startTimeField = uieditfield(fig, "numeric", ...
    "Position", [686, 635, 95, 32], ...
    "BackgroundColor", inputBackgroundColor, ...
    "FontColor", textColor, ...
    "ValueChangedFcn", @onControlsChanged);

uilabel(fig, ...
    "Text", "End Time (s):", ...
    "Position", [798, 640, 85, 22], ...
    "FontColor", textColor);

endTimeField = uieditfield(fig, "numeric", ...
    "Position", [886, 635, 95, 32], ...
    "BackgroundColor", inputBackgroundColor, ...
    "FontColor", textColor, ...
    "ValueChangedFcn", @onControlsChanged);

uibutton(fig, "push", ...
    "Text", "Full Range", ...
    "Position", [994, 635, 100, 32], ...
    "BackgroundColor", buttonColor, ...
    "FontColor", buttonTextColor, ...
    "ButtonPushedFcn", @onResetRange);

plotAxes = uiaxes(fig, ...
    "Position", [20, 90, 780, 525], ...
    "Color", inputBackgroundColor, ...
    "Box", "on");
grid(plotAxes, "on");
title(plotAxes, "Load CSV or generate synthetic data to begin");
xlabel(plotAxes, "Time (s)");
ylabel(plotAxes, "Value");
stylePlotAxes();

summaryPanel = uipanel(fig, ...
    "Title", "Battery Summary", ...
    "Position", [820, 90, 274, 525], ...
    "BackgroundColor", summaryPanelColor, ...
    "ForegroundColor", textColor);

summaryRangeLabel = uilabel(summaryPanel, ...
    "Text", "Displayed Range: --", ...
    "Position", [18, 470, 238, 24], ...
    "FontWeight", "bold", ...
    "FontColor", textColor);

uilabel(summaryPanel, ...
    "Text", "Min Voltage", ...
    "Position", [18, 420, 120, 20], ...
    "FontWeight", "bold", ...
    "FontColor", textColor);
minVoltageValueLabel = uilabel(summaryPanel, ...
    "Text", "--", ...
    "Position", [18, 392, 220, 24], ...
    "FontSize", 16, ...
    "FontColor", textColor);

uilabel(summaryPanel, ...
    "Text", "Max Voltage", ...
    "Position", [18, 350, 120, 20], ...
    "FontWeight", "bold", ...
    "FontColor", textColor);
maxVoltageValueLabel = uilabel(summaryPanel, ...
    "Text", "--", ...
    "Position", [18, 322, 220, 24], ...
    "FontSize", 16, ...
    "FontColor", textColor);

uilabel(summaryPanel, ...
    "Text", "Max Temperature", ...
    "Position", [18, 280, 140, 20], ...
    "FontWeight", "bold", ...
    "FontColor", textColor);
maxTemperatureValueLabel = uilabel(summaryPanel, ...
    "Text", "--", ...
    "Position", [18, 252, 220, 24], ...
    "FontSize", 16, ...
    "FontColor", textColor);

uilabel(summaryPanel, ...
    "Text", "Average Current", ...
    "Position", [18, 210, 140, 20], ...
    "FontWeight", "bold", ...
    "FontColor", textColor);
avgCurrentValueLabel = uilabel(summaryPanel, ...
    "Text", "--", ...
    "Position", [18, 182, 220, 24], ...
    "FontSize", 16, ...
    "FontColor", textColor);

uilabel(summaryPanel, ...
    "Text", "SOC Start", ...
    "Position", [18, 140, 120, 20], ...
    "FontWeight", "bold", ...
    "FontColor", textColor);
socStartValueLabel = uilabel(summaryPanel, ...
    "Text", "--", ...
    "Position", [18, 112, 220, 24], ...
    "FontSize", 16, ...
    "FontColor", textColor);

uilabel(summaryPanel, ...
    "Text", "SOC End", ...
    "Position", [18, 70, 120, 20], ...
    "FontWeight", "bold", ...
    "FontColor", textColor);
socEndValueLabel = uilabel(summaryPanel, ...
    "Text", "--", ...
    "Position", [18, 42, 220, 24], ...
    "FontSize", 16, ...
    "FontColor", textColor);

statusLabel = uilabel(fig, ...
    "Text", "Ready.", ...
    "Position", [20, 30, 950, 30], ...
    "HorizontalAlignment", "left", ...
    "FontColor", textColor);

exportButton = uibutton(fig, "push", ...
    "Text", "Export Data", ...
    "Position", [984, 25, 110, 32], ...
    "BackgroundColor", buttonColor, ...
    "FontColor", buttonTextColor, ...
    "Enable", "off", ...
    "ButtonPushedFcn", @onExportData);

tryLoadDefaultSample();

    function tryLoadDefaultSample
        samplePath = fullfile(pwd, 'sample_battery_data.csv');
        if ~isfile(samplePath)
            return;
        end

        try
            loadedTable = readtable(samplePath);
            dataTable = normalize_battery_table(loadedTable);
            syncTimeControlsToData();
            updatePlot();
            exportButton.Enable = "on";
            statusLabel.Text = "Loaded default sample_battery_data.csv";
        catch
            % Ignore preload errors; user can load/generate manually.
        end
    end

    function onLoadCsv(~, ~)
        [fileName, filePath] = uigetfile("*.csv", "Select Battery CSV");
        if isequal(fileName, 0)
            statusLabel.Text = "CSV load canceled.";
            return;
        end

        fullPath = fullfile(filePath, fileName);

        try
            loadedTable = readtable(fullPath);
            dataTable = normalize_battery_table(loadedTable);
            syncTimeControlsToData();
            updatePlot();
            exportButton.Enable = "on";
            statusLabel.Text = sprintf("Loaded: %s", fullPath);
        catch ME
            uialert(fig, ME.message, "CSV Load Error");
            statusLabel.Text = "Failed to load CSV.";
        end
    end

    function onGenerateSynthetic(~, ~)
        outputPath = fullfile(pwd, 'sample_battery_data.csv');

        try
            generatedTable = randomized_data_generator(outputPath, false);
            dataTable = normalize_battery_table(generatedTable);
            syncTimeControlsToData();
            updatePlot();
            exportButton.Enable = "on";
            statusLabel.Text = sprintf("Generated synthetic data: %s", outputPath);
        catch ME
            uialert(fig, ME.message, "Generation Error");
            statusLabel.Text = "Synthetic data generation failed.";
        end
    end

    function onExportData(~, ~)
        if isempty(dataTable)
            uialert(fig, "Load or generate battery data before exporting.", "No Data");
            statusLabel.Text = "No data available to export.";
            return;
        end

        defaultFileName = sprintf( ...
            "battery_report_%s.html", ...
            datestr(now, "yyyymmdd_HHMMSS"));
        [fileName, filePath] = uiputfile("*.html", "Save Battery Report", defaultFileName);
        if isequal(fileName, 0)
            statusLabel.Text = "Report export canceled.";
            return;
        end

        statusLabel.Text = "Generating published battery report...";
        drawnow;

        try
            selectedRange = [startTimeField.Value, endTimeField.Value];
            reportPath = export_battery_report( ...
                dataTable, ...
                selectedRange, ...
                fullfile(filePath, fileName));
            statusLabel.Text = sprintf("Report exported: %s", reportPath);
            uialert(fig, sprintf("Report saved to:\n%s", reportPath), "Export Complete");
        catch ME
            uialert(fig, ME.message, "Export Error");
            statusLabel.Text = "Report export failed.";
        end
    end

    function onControlsChanged(~, ~)
        if isempty(dataTable)
            return;
        end
        updatePlot();
    end

    function onResetRange(~, ~)
        if isempty(dataTable)
            return;
        end
        syncTimeControlsToData();
        updatePlot();
    end

    function syncTimeControlsToData
        tMin = dataTable.Time_s(1);
        tMax = dataTable.Time_s(end);

        if tMax <= tMin
            tMax = tMin + 1;
        end

        startTimeField.Limits = [tMin, tMax];
        endTimeField.Limits = [tMin, tMax];
        startTimeField.Value = tMin;
        endTimeField.Value = tMax;
    end

    function updatePlot
        if isempty(dataTable)
            cla(plotAxes);
            title(plotAxes, "Load CSV or generate synthetic data to begin");
            xlabel(plotAxes, "Time (s)");
            ylabel(plotAxes, "Value");
            stylePlotAxes();
            resetSummaryMetrics();
            return;
        end

        selectedRange = [startTimeField.Value, endTimeField.Value];
        if selectedRange(1) > selectedRange(2)
            selectedRange = fliplr(selectedRange);
            startTimeField.Value = selectedRange(1);
            endTimeField.Value = selectedRange(2);
        end

        appliedRange = battery_visualization(plotAxes, dataTable, plotDropdown.Value, selectedRange);
        stylePlotAxes();
        updateSummaryMetrics(appliedRange);

        statusLabel.Text = sprintf( ...
            "Displaying %s (%.1f s to %.1f s)", ...
            plotDropdown.Value, appliedRange(1), appliedRange(2));
    end

    function stylePlotAxes
        plotAxes.XColor = textColor;
        plotAxes.YColor = textColor;
        plotAxes.Title.Color = textColor;
        plotAxes.XLabel.Color = textColor;
        plotAxes.YLabel.Color = textColor;
    end

    function updateSummaryMetrics(appliedRange)
        if isempty(dataTable)
            resetSummaryMetrics();
            return;
        end

        summary = summarize_battery_data(dataTable, appliedRange);
        summaryRangeLabel.Text = sprintf( ...
            "Displayed Range: %.1f s to %.1f s", ...
            summary.AppliedRange_s(1), summary.AppliedRange_s(2));
        minVoltageValueLabel.Text = sprintf("%.2f V", summary.MinVoltage_V);
        maxVoltageValueLabel.Text = sprintf("%.2f V", summary.MaxVoltage_V);
        maxTemperatureValueLabel.Text = sprintf("%.2f C", summary.MaxTemperature_C);
        avgCurrentValueLabel.Text = sprintf("%.2f A", summary.AverageCurrent_A);
        socStartValueLabel.Text = sprintf("%.2f %%", summary.SOCStart_pct);
        socEndValueLabel.Text = sprintf("%.2f %%", summary.SOCEnd_pct);
    end

    function resetSummaryMetrics
        summaryRangeLabel.Text = "Displayed Range: --";
        minVoltageValueLabel.Text = "--";
        maxVoltageValueLabel.Text = "--";
        maxTemperatureValueLabel.Text = "--";
        avgCurrentValueLabel.Text = "--";
        socStartValueLabel.Text = "--";
        socEndValueLabel.Text = "--";
    end
end
